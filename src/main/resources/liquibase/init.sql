(
    id             BIGINT generated by default as identity primary key,
    chat_id        BIGINT    NOT NULL,
    user_name      VARCHAR(20),
    task_date_time TIMESTAMP NOT NULL,
    phone          VARCHAR(20),
    is_active         BOOLEAN

);
