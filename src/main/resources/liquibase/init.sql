CREATE TABLE cat
(
    id               BIGINT generated by default as identity primary key,
    breed            VARCHAR(20),
    cat_name         VARCHAR(20),
    year_of_birthday INT,
    description      VARCHAR

);
CREATE TABLE dog
(
    id               BIGINT generated by default as identity primary key,
    breed            VARCHAR(20),
    dog_name         VARCHAR(20),
    year_of_birthday INT,
    description      VARCHAR

);
CREATE TABLE users
(
    id      BIGINT generated by default as identity primary key,
    chat_id BIGINT NOT NULL UNIQUE,
    phone   INT
);
